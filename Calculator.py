
class Calculator:
    def add(self, num1, num2):
        add = num1 + num2
        return add

    def sub(self, num1, num2):
        subSum = num1 - num2
        return subSum

    def mult(self, num1, num2):
        multSum = num1 * num2
        return multSum

    def div(self, num1, num2):
        divSum = num1 / num2
        return divSum